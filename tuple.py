# creating and printing a tuple
vacation_location = ("seychelles","miami", "santorini","florida","obudu")
print(vacation_location)

#using methods in a tuple
# 1.) using count() method in counting the item "obudu" in vacation_location
vacation_location.count("obudu")

# 2.) using len() method to print out the length of the tuple created
len(vacation_location)

# 3.) using index() method to find the index of "seychelles in the tuple" 
vacation_location.index("seychelles")

# 4.) using sort() method to sort tuple
print(any(vacation_location))

# 5.) using len() and max() method in the git tuple created
print(len(max(vacation_location)))